#include "libft.h"

int	ft_intlen(int n)
{
	int	len;

	len = 1;
	if (n < 0)
	{
		len++;
		n = -n;
	}
	while ((n / 10) > 0)
	{
		len++;
		n /= 10;
	}
	return (len);
}
